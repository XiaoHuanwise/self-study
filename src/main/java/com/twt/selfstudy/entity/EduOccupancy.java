package com.twt.selfstudy.entity;

public class EduOccupancy {
    private String classroomId;
    private String year;
    private String weekday;
    private String weekState;
    private String weekStateNum;
    private String startTime;
    private String endTime;
    private String id;

    public String getClassroomId() {
        return classroomId;
    }

    public void setClassroomId(String classroomId) {
        this.classroomId = classroomId;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getWeekday() {
        return weekday;
    }

    public void setWeekday(String weekday) {
        this.weekday = weekday;
    }

    public String getWeekState() {
        return weekState;
    }

    public void setWeekState(String weekState) {
        this.weekState = weekState;
    }

    public String getWeekStateNum() {
        return weekStateNum;
    }

    public void setWeekStateNum(String weekStateNum) {
        this.weekStateNum = weekStateNum;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
